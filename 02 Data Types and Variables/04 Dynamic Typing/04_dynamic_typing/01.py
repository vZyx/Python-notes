

var = 55

# change value in memory
var = 10
print(var)          # 10

# Get, Change, Assign
var = var + 5
print(var)          # 15

# Change the DATA type to floating point
# Python uses Dynamic typing: Change types
# Set memory value to 25.5
var = 6.5          # 6.5
print(var)

var = 3 * var - var
print(var)          # 13.0

# Change type again to string
var = 'Dr Mostafa'
print(var)          # Dr Mostafa
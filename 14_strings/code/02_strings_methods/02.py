
print(' '.isspace())        # True
print('\n'.isspace())       # True
print('\t'.isspace())       # True

print('\n\tHello   \t'.strip())     # Hello

print('Hi Jack? Hi'.replace('Hi', 'Most'))  # Most Jack? Most

print('\nI am     33 '.split())     # ['I', 'am', '33']

print('Hey\nHow\nAre you?'.splitlines())   # ['Hey', 'How', 'Are you?']





# Not in: Opposite of 'in' meaning
name = 'mostafa'
# All prints are False
print('mostafa' not in name)
print('most' not in name)
print('m' not in name)
print('' not in name)
print('ostafa' not in name)
print('sta' not in name)

# All prints are True
print('Mostafa' not in name)
print('mofa' not in name)
print('aft' not in name)


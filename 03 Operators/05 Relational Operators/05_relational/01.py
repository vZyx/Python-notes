

status = True
print(status)     # True
# not flips true to false and false to true
print(not status) # False

print(bool('0'))  # True
print(bool('1'))  # True
print(bool(''))   # False

print(bool(5))     # True
print(bool(-5))    # True
print(bool(5.5))   # True
print(bool(0))     # False

# bool():
# False for empty string and zero
# True otherwise


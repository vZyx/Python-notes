
num = 12345

print(num / 10)         #  1234.5
print(num / 100)        #  123.45
print(num / 1000)       #  12.345
print(num / 10000)      #  1.2345
print(num / 100000)     # 0.12345

# Remove last digits
print(num // 10)        # 1234
print(num // 100)       # 123
print(num // 1000)      # 12
print(num // 10000)     # 1
print(num // 100000)    # 0




print(14 / 2)   # 7.0
print(14 / 3)   # 4.666666666666667
print(14 / 40)  # 0.35

print(14 // 2)   # 7
print(14 // 3)   # 4
print(14 // 40)  # 0

print(14 / 8)     # 1.75
print(14 // 8)    # 1   round down to a small value 1 = min(1, 2)
print(-14 // 8)   # -2  = -2 = min(-2, -1)

print(type(14 / 7))     # float
print(type(14 // 7))    # int

